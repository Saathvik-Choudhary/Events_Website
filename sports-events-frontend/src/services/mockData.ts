// Helper functions for mock API responses
export const createMockApiResponse = <T>(data: T[], page: number = 0, size: number = 12) => {
  const start = page * size;
  const end = start + size;
  const content = data.slice(start, end);
  
  return {
    content,
    pageable: {
      pageNumber: page,
      pageSize: size,
      sort: {
        empty: true,
        sorted: false,
        unsorted: true,
      },
      offset: start,
      paged: true,
      unpaged: false,
    },
    last: end >= data.length,
    totalElements: data.length,
    totalPages: Math.ceil(data.length / size),
    size,
    number: page,
    sort: {
      empty: true,
      sorted: false,
      unsorted: true,
    },
    first: page === 0,
    numberOfElements: content.length,
    empty: content.length === 0,
  };
};

